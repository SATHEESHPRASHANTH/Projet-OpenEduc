            <h2>À propos de l'APEA</h2>
            <p>L'Association de Parents d'Élèves d'Alsace (APEA) est une organisation à but non lucratif engagée dans la promotion et la défense des intérêts des parents d'élèves au sein des établissements publics locaux d'enseignement en Alsace.</p>

            <p>Nous jouons un rôle actif en tant qu'intermédiaire entre les parents et le personnel pédagogique, participant ainsi au fonctionnement du service public de l'éducation. Notre mission principale est de contribuer aux actions scolaires et de défendre les intérêts moraux et matériels communs aux parents d'élèves.</p>
        </section>

        <section>
            <h2>Objectifs et Engagements</h2>
            <p>L'APEA s'engage à :</p>
            <ul>
                <li>Participer activement aux conseils d'école, conseils d'administration des établissements, et autres instances éducatives.</li>
                <li>Défendre les droits et intérêts des parents d'élèves au sein du système éducatif.</li>
                <li>Promouvoir la collaboration entre les parents, les enseignants et les autorités éducatives.</li>
                <li>Contribuer au bien-être des élèves en organisant des activités éducatives, culturelles et sportives.</li>
            </ul>
        </section>

        <section>
            <h2>Structure de l'APEA</h2>
            <p>L'APEA est organisée de la manière suivante :</p>
            <ul>
                <li><strong>Conseil d'Administration :</strong> Composé de membres élus, il prend les décisions importantes et guide les actions de l'association.</li>
                <li><strong>Bureaux Locaux :</strong> Présents dans chaque école, les bureaux locaux travaillent en étroite collaboration avec les référents APEA et les parents d'élèves.</li>
                <li><strong>Comités Thématiques :</strong> Créés en fonction des besoins, ces comités se concentrent sur des sujets spécifiques tels que les événements, les projets éducatifs, etc.</li>
            </ul>
        </section>

        <section>
            <h2>Projets et Initiatives</h2>
            <p>L'APEA est impliquée dans divers projets et initiatives, notamment :</p>
            <ul>
                <li><strong>Achats Groupés :</strong> Nous facilitons l'achat groupé de fournitures scolaires, de livres et d'autres articles essentiels pour les élèves.</li>
                <li><strong>Organisation d'Événements :</strong> Nous participons à la planification et à la réalisation d'événements éducatifs, culturels et sportifs au bénéfice des élèves.</li>
                <li><strong>Actions Sociales :</strong> Nous soutenons des actions sociales telles que des collectes de fonds pour des projets éducatifs spécifiques et des initiatives caritatives.</li>
            </ul>
